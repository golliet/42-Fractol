#include "libft.h"
#include <stdlib.h>

void	ft_lstdel(t_listlibft **alst, void (*del)(void *, size_t))
{
	t_listlibft	*list;

	list = *alst;
	while (list)
	{
		del(list->content, list->content_size);
		free(list);
		list = list->next;
	}
	*alst = NULL;
}
